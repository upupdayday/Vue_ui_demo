<template>
    <div class="col" :class="colClass" :style="colStyle">
        <slot></slot>
    </div>
</template>
<script>
    export default {
        name: "gCol",
        data () {
            return{
                gutter:0,
            }
        },
        props:{
            span:{
                type:[Number,String]
            },
            offset:{
                type:[Number,String]
            }
        },
        computed:{
            colStyle(){
                let {gutter} = this;
                return{
                    paddingLeft: gutter/2+'px',
                    paddingRight: gutter/2+'px'
                }
            },
            colClass(){
                let {span, offset} = this
                return [
                        span && `col-${span}`,
                        offset &&`offset-${offset}`
                ]
            }
        }
    }
</script>
<style lang="scss" scoped>
    .col {
        $class-prefix: col-;
        @for $n from 1 through 24{
            &.#{$class-prefix}#{$n} {
                width: ($n/24)*100%;
            }
        }

        $class-prefix: offset-;
        @for $n from 1 through 24{
            &.#{$class-prefix}#{$n} {
                margin-left: ($n/24)*100%;
            }
        }
    }
</style>